# addition 
print(8 + 10)

# substraction
print(8 - 10)

# multiplication 
print(8 * 2)

# Exponentation
print(8 ** 2)

# Division - produces a float 
print(65 / 8)

# Quotient - produces an integer (leaves off the fractional part)
print(65 // 8)

# Remainder or modulus 
print(65 % 8)

# Multi-line expressions, use a \ 
print(3 + 4 * \
      2 ** 5)

# Mixed-mode arithmetic involves integers
# and floating-point numbers
print(3.14 * 3 **2)

